package learning;

public class ArithmenticOperatorDemo {
	
	public static void main(String[] args) {
		
		int num1 = 25;
		int num2 = 10;
		
		//+
		System.out.println(num1+num2);//+: addition operators
		
		//-
		System.out.println(num1-num2);//-: substraction operators
		
		//*
		System.out.println(num1*num2);//*: mutlipliction operators
		
		// /
		System.out.println(num1/num2);// / : division operators
		
		
		// %
		
		System.out.println(num1%num2);//+: modulus operators

		
	}
}
